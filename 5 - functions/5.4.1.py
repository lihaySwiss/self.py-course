def func(num1, num2):
    """
    Function will print multiplication result of two parameters
    :param num1: first parameter
    :type num1: int
    :param num2: second parameter
    :type num2: int
    :return: multiplication result
    :rtype: int
    """
    return num1*num2

def main():
    func(3,4)

if __name__ == "__main__":
    main()